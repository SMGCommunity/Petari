            u16 whole = F32ToU16(abs_fidx);
            f32 frac = abs_fidx - U16ToF32(whole);

            f32 cos = detail::gSinCosTbl[whole & 255].cos_val + frac * detail::gSinCosTbl[whole & 255].cos_delta;

            return cos;
